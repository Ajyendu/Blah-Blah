// Map human-readable section titles to simple category IDs
const CATEGORY_IDS = {
  "Smileys & People": "smileys",
  "Animals & Nature": "animals",
  "Food & Drink": "food",
  Activity: "activity",
  "Travel & Places": "travel",
  Objects: "objects",
  Symbols: "symbols",
  Flags: "flags",
};

const CATEGORY_ORDER = [
  "smileys",
  "animals",
  "food",
  "activity",
  "travel",
  "objects",
  "symbols",
  "flags",
];

const CATEGORY_LABELS = {
  smileys: "Smileys",
  animals: "Animals",
  food: "Food",
  activity: "Activity",
  travel: "Travel",
  objects: "Objects",
  symbols: "Symbols",
  flags: "Flags",
};

// Parse the raw text into category-specific emoji arrays
const emojisByCategory = {};
let currentCategory = null;

RAW_EMOJI_TEXT.split("\n").forEach((rawLine) => {
  const line = rawLine.trim();
  if (!line) return;
  if (line === "Advertisement") return;

  // If this line is a section heading, switch category
  if (CATEGORY_IDS[line]) {
    currentCategory = CATEGORY_IDS[line];
    if (!emojisByCategory[currentCategory]) {
      emojisByCategory[currentCategory] = [];
    }
    return;
  }

  // Skip any stray ASCII-only words/symbols (like '&' from headings)
  if (/^[A-Za-z0-9& ]+$/.test(line)) return;

  if (!currentCategory) return;
  if (!emojisByCategory[currentCategory]) {
    emojisByCategory[currentCategory] = [];
  }
  emojisByCategory[currentCategory].push(line);
});

// Public structure: ordered list of categories with labels and emojis
export const EMOJI_CATEGORIES = CATEGORY_ORDER.map((id) => ({
  id,
  label: CATEGORY_LABELS[id],
  emojis: emojisByCategory[id] || [],
}));

// Optional: flattened list of all emojis (kept for potential future use)
export const EMOJI_ALL = EMOJI_CATEGORIES.flatMap((c) => c.emojis);


